SELECT C.STUDENT_ID, TEST_NAME
FROM STUDENT C
JOIN STUDENT_TEST ON C.STUDENT_ID = STUDENT_TEST.STUDENT_ID
JOIN TEST ON STUDENT_TEST.TEST_ID = TEST.TEST_ID
WHERE TEST_NAME LIKE '%Информационная безопасность%' AND SCORE <> 0;

